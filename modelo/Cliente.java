package org.mdiaz.appfacturas.modelo;

public class Cliente {
    private String nombre;
    private String cuil;

    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public String getCuil() {
        return cuil;
    }
    public void setCuil(String cuil) {
        this.cuil = cuil;
    }
}
